                    <h2>遺言書の役割</h2>
                    <p>遺言書とは、被相続人（遺言者）が自分の死後に自分の相続財産（遺産）を、誰にどれだけ分配するかを記載した書状のことです。遺言書を民法の規定に従って正しく作成すれば、そのことで故人の「最終的な意思表示」として強力な法的効力を持ちます。その役割は主に次の2つです。<br>
                            ・遺族が財産の扱いについて迷わないようにするため<br>
                            財産をどのように分配するかをあらかじめ決めておけば、遺族が協議などを行う手間が省けます。<br>
                            ・遺産トラブルを防ぐため<br>
                            遺族の中でも、特に相続権を持つ相続人同士が財産を巡って権利を主張し合い、トラブルに発展するような事態を防ぐことができます。実際に遺族が相続について揉め、血縁者同士で対立してしまうことはよくあることです。これを防ぐためにも遺言書は有効です</p>
                    <h2>遺言書の種類</h2>
                    <p>遺言書にはいくつか種類があります。代表的なものは次の3つです。<br>
                            ・公正証書遺言<br>
                            公証役場で公証人（公権力を根拠に証明・認証する人）に作成してもらう遺言書です。専門家に相談しながら作成してもらうため安心・確実ですが、ある程度の時間と費用がかかります。手数料は遺言書に書く財産の額に応じて変わります。また、遺言者の真意を証明するため、「2人以上の証人」の立ち会いが必要です。完成すると公証役場から遺言公正証書（正本、謄本の各1部）が遺言者に交付されます。<br>
                            ・自筆証書遺言<br>
                            紙とペンがあれば作成可能な、被相続人が自分で書く最もシンプルな遺言書です。民法に「その全文、日付及び氏名を自書し」という規定があり、すべて手書きで作成する必要があります。パソコン、代筆、音声、映像などで残したとしたものは無効です。遺言の内容、遺言者の氏名、作成年月日を書面に記し、自身の印鑑を押印します。<br>
                            ・秘密証書遺言<br>
                            遺言内容を秘密にしたまま、遺言書の存在のみが証明される遺言書です。こちらの遺言内容は自筆である必要はなく、パソコンの使用、代筆が可能です。遺言内容に遺言者が署名（署名は自筆の必要あり）と押印し、封筒に入れて封をし、封印にも同じ印鑑を使って押印した上で、公証人に提示して所定の処理をしてもらいます。</p>
                    <h2>遺言書は書き直せるの？</h2>
                    <p>遺言書は遺言者本人によっていつでも撤回し、書き直すことができます。基本的に遺言は最後に書いたものが有効になり、新しい日付の遺言がそれ以前の遺言の内容と異なるときは、前の遺言の抵触する部分が撤回されたものとみなされます。
                            よりしっかりと書き直すなら、まず以前の遺言を撤回し、新しい遺言書を作成します。撤回だけをするなら、公正証書遺言、自筆証書遺言、秘密証書遺言のいずれかの方式で「以前の遺言を撤回する」という内容の遺言を記します。新しい遺言書を作成する場合は、冒頭などに前回の遺言書を撤回するという文章を記載すればOKです。</p>
                    <h2>遺言書で残せる内容</h2>
                    <p>遺言書で残すことができるのは相続財産のことだけではありません。その内容は法律で定められています。代表的なものは次の3つです。<br>
                            ・財産分与について<br>
                            預金、株式、生命保険、不動産などの相続人を指定するものです。誰に何を相続させるかという遺産分割方法、各相続人がどれだけ受け取るようにするかという相続分の指定などができます。<br>
                            ・遺言執行人の指名<br>
                            確実に遺言が実行できるよう、遺言執行人を指名することができます。遺言執行人とは、自分の死後に遺言書の内容を実行する手続きをしてくれる人のことです。<br>
                            ・祭祀承継者（さいしけいしょうしゃ）の指名<br>
                            お墓や仏壇などを承継し、管理などを行う祭祀承継者を指名できます。祭祀承継は遺産相続とは別のものとみなされ、相続税はかかりません。また、相続人が相続放棄をしても、祭祀承継者としてお墓などを承継することは可能です。
                            遺言書を残すことは、遺族に対して自分の最終的な意思表示を行うことです。いつでも書き直しができ、むしろそのときどきの状況によって書き直すべきものという認識を持って作成に取り掛かってみてはいかがでしょうか。</p>
                  
