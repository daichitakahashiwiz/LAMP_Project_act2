                <section>
                      <h2>生前整理とは</h2>
                    <p>人が亡くなったときに、遺された家族が遺品を片付ける「遺品整理」という言葉があります。これに対し「生前整理」は元気なうちに、身辺をある程度片付けておくことをいいます。
                        時間的にも体力的にも余裕があるうちに、亡くなった後のことを考えて行うのが生前整理であり、近年は高齢者だけでなく、20代や30代といった若い世代で行う方もいるようです。
                        老いる前に身辺の整理する場合は「老前整理」とも呼ばれ、事故や災害によって急に命を落とす可能性は、誰しもゼロではないので、亡くなった後のことを考えて身の回りを整えておくのに「早い、遅い」はないといえます。</p>
                    <h2>生前整理は遺される家族のため</h2>
                    <p>生前整理を行う大きな目的は、亡くなった後、家族に苦労をかけないためです。何の準備もなく亡くなった方の遺品を整理するのは、想像以上に大変なことです。特に最近は、独り暮らしの高齢者が増えており、生前の膨大な遺品の山を片付けるのは肉体的にも精神的にも負担が大きいといいます。実際、遺品整理に携わったことのある人は口を揃えて、「二度とやりたくない」というほどです。
                       たとえ独り暮らしでなくても、残された家族にこうした苦労をかけないため、物を減らしたり、亡くなった後の物の行先を決めておいたりする生前整理は大切なのです。</p>
                    <h2>実は自分のためにもなる</h2>
                    <p>生前整理を行うことは、家族だけでなく自分自身のためにもなります。なぜなら、生前整理は自分の人生を棚卸して、本当に必要なものだけにシンプル化することでもあるからです。
                       物だけでなく、もつれた人間関係やお金、時間などの問題を片付け、本当に必要なものだけで暮らすことができれば、今をより良く生きることができるはずです。若い世代にも生前整理が広まっているのは、こうした理由もあると考えられます。</p>
                    <h2>生前整理の進め方</h2>
                    <p>生前整理で主に手をつけるのは、「財産」と「物」です。まずは、何をどのくらい持っているのかを把握するところから始めましょう。</p>
                    <h2>財産目録を作る</h2>
                    <p>財産とは、現金や預貯金のほか、家や土地、車、有価証券、宝石や骨董品などのことです。財産については、何を持っているのかを把握した上で、財産目録を作ることをおすすめします。
                       人が亡くなった後には相続が起こり、遺族は相続財産を分けることになりますが、財産がどれだけあるのかを調べるのに時間がかかるので、あらかじめ目録を作っておいてあげると、遺族がその手間を省くことができます。 
                       また、財産を早めに把握しておけば、相続税の対策を行うことも可能になります。場合によっては生前贈与などで税金対策をすることができるので、目録を作ったら弁護士や税理士などの専門家に相談してみてもいいでしょう。</p>
                    <h2>要る物・要らない物を整理する</h2>
                    <p>亡くなった後、大量に物が残されていれば、家族はその処分に頭を悩ませることになります。また、多くの物に囲まれた生活では、「今」を生きていく場合でも決して居心地は良くないはずです。洋服や思い出の品など、ついつい溜め込んでしまう物もあるでしょうが、本当に必要な物以外は処分する作業に取り組みましょう。何も、全てを捨てなければならないわけではありません。何を処分して何を残すのかを見極めることが大切なのです。    
                        最近は、お片付けや断捨離についての情報もたくさんあるので、それらを参考にしながら、少しずつ身辺を身軽にしていきましょう。</p>
                    <h2>遺言書</h2>
                    <p>特に財産の行方について、確実に自分の意思を示しておきたい場合は遺言書を作ることをおすすめします。弁護士や公証役場のサポートを受けて作成した正式な遺言書には、法的効力を持つからです。
                       遺言書がなくても、民法の規定に従って遺産分割はされますが、法で定められた相続人以外の人に財産を引き継ぎたかったり、特定の財産を特定の人に遺したかったりする場合には、正式な遺言書で示しておかなければ叶わない可能性があります。自分の死後、家族が遺産分割で揉めないためにも、遺言書を作っておくのがいいでしょう。</p>
